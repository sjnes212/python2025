#print a program "hello world"
# using a while loop any number of times from user input
# This code will print "Hello World" a specified number of times based on user input

n=int(input("Enter the number of times to print 'Hello World': "))
i=0
while i < n:
    # if n=20 loop will run 0 to 19
    print("Hello World")
    i=i + 1
    
